/**
 * serverBundler.ts — 构建时生成 Koa 服务器壳子
 *
 * 职责：
 *   1. 从 MongoDB 读取应用的所有云函数代码
 *   2. 生成 server/index.ts（Koa 入口，注册所有函数路由，注入 ORM ctx）
 *   3. 生成 server/package.json（最小依赖：koa + @koa/router + koa-body + mongoose）
 *   4. 输出到构建产物目录的 server/ 子目录
 *
 * 生成的服务器路由规则：每个云函数映射为 POST /functions/:functionName
 */

import * as fs from 'fs'
import * as path from 'path'
import { FunctionService } from '../FunctionService.js'

// ── 类型 ──────────────────────────────────────────────────────────────────────

export interface ServerBundleOptions {
  /** 应用 ID */
  appId: string
  /** 输出目录（绝对路径），生成的 server/ 文件将写入此目录 */
  outputDir: string
}

// ── 主函数 ────────────────────────────────────────────────────────────────────

/**
 * 生成 Koa 服务器壳子
 * 输出结构：
 *   outputDir/
 *     server/
 *       index.js        — Koa 入口
 *       functions/      — 各云函数文件
 *         getUserList.js
 *         createOrder.js
 *       package.json    — 最小依赖
 */
export async function bundleServer(options: ServerBundleOptions): Promise<void> {
  const { appId, outputDir } = options

  // 读取应用的所有云函数
  const functions = await FunctionService.listFunctions(appId)

  if (functions.length === 0) {
    // 没有云函数，跳过服务器壳子生成
    return
  }

  const serverDir = path.join(outputDir, 'server')
  const functionsDir = path.join(serverDir, 'functions')

  // 创建目录
  fs.mkdirSync(serverDir, { recursive: true })
  fs.mkdirSync(functionsDir, { recursive: true })

  // 1. 生成各云函数文件
  for (const fn of functions) {
    const fnCode = generateFunctionFile(fn.name, fn.code)
    fs.writeFileSync(path.join(functionsDir, `${fn.name}.js`), fnCode, 'utf-8')
  }

  // 2. 生成 Koa 入口文件
  const entryCode = generateEntryFile(functions.map((fn) => fn.name), appId)
  fs.writeFileSync(path.join(serverDir, 'index.js'), entryCode, 'utf-8')

  // 3. 生成 package.json
  const packageJson = generatePackageJson()
  fs.writeFileSync(path.join(serverDir, 'package.json'), JSON.stringify(packageJson, null, 2), 'utf-8')
}

// ── 代码生成辅助 ──────────────────────────────────────────────────────────────

/**
 * 生成单个云函数文件
 * 导出一个 async handler(input, ctx) 函数
 */
function generateFunctionFile(name: string, code: string): string {
  return `/**
 * Cloud Function: ${name}
 * Auto-generated by Banyuan build service
 */

module.exports = async function ${name}(input, ctx) {
  ${code}
};
`
}

/**
 * 生成 Koa 入口文件
 */
function generateEntryFile(functionNames: string[], appId: string): string {
  const imports = functionNames
    .map((name) => `const fn_${name} = require('./functions/${name}.js');`)
    .join('\n')

  const routes = functionNames
    .map((name) => `
  router.post('/functions/${name}', async (ctx) => {
    const input = ctx.request.body || {};
    try {
      const result = await fn_${name}(input, fnCtx);
      ctx.body = { success: true, data: result };
    } catch (err) {
      ctx.status = 500;
      ctx.body = { success: false, message: err.message || String(err) };
    }
  });`)
    .join('\n')

  return `/**
 * Banyuan App Server — Auto-generated
 * App ID: ${appId}
 *
 * 每个云函数注册为 POST /functions/:functionName
 */

const Koa = require('koa');
const Router = require('@koa/router');
const { koaBody } = require('koa-body');
const mongoose = require('mongoose');

// ── 云函数导入 ──
${imports}

// ── 应用配置 ──
const PORT = process.env.PORT || 3003;
const MONGO_URI = process.env.MONGO_URI || 'mongodb://localhost:27017/banyuan';
const APP_ID = '${appId}';

// ── 初始化 ──
const app = new Koa();
const router = new Router();

app.use(koaBody());

// 错误处理
app.use(async (ctx, next) => {
  try {
    await next();
  } catch (err) {
    ctx.status = err.status || 500;
    ctx.body = { success: false, message: err.message || 'Internal Server Error' };
  }
});

// ── 云函数上下文（简化版 ORM） ──
// 生产环境中，此处会基于 AppSchema 动态构建 Mongoose Model
const fnCtx = {
  appId: APP_ID,
  env: process.env,
  db: new Proxy({}, {
    get(_, collectionName) {
      const mongoCollectionName = \`app_\${APP_ID}_\${String(collectionName)}\`;
      // 延迟获取/创建 Model
      let model;
      try {
        model = mongoose.model(mongoCollectionName);
      } catch {
        const schema = new mongoose.Schema({}, { strict: false, timestamps: true });
        model = mongoose.model(mongoCollectionName, schema, mongoCollectionName);
      }
      return {
        find: (filter = {}, options = {}) => {
          let query = model.find(filter);
          if (options.sort) query = query.sort(options.sort);
          if (options.skip) query = query.skip(options.skip);
          if (options.limit) query = query.limit(options.limit);
          return query.exec();
        },
        findOne: (filter) => model.findOne(filter).exec(),
        findById: (id) => model.findById(id).exec(),
        create: (data) => model.create(data),
        updateById: (id, data) => model.findByIdAndUpdate(id, { $set: data }, { new: true }).exec(),
        deleteById: async (id) => {
          const result = await model.findByIdAndDelete(id).exec();
          return result !== null;
        },
        count: (filter = {}) => model.countDocuments(filter).exec(),
      };
    },
  }),
};

// ── 路由注册 ──
router.get('/health', (ctx) => {
  ctx.body = { success: true, message: 'App server is running', appId: APP_ID };
});
${routes}

app.use(router.routes());
app.use(router.allowedMethods());

// ── 启动 ──
async function start() {
  await mongoose.connect(MONGO_URI);
  console.log('[Server] Connected to MongoDB');

  app.listen(PORT, () => {
    console.log(\`[Server] App server running on port \${PORT}\`);
    console.log(\`[Server] Functions: ${functionNames.join(', ')}\`);
  });
}

start().catch((err) => {
  console.error('[Server] Failed to start:', err);
  process.exit(1);
});
`
}

/**
 * 生成最小 package.json
 */
function generatePackageJson(): Record<string, unknown> {
  return {
    name: 'banyuan-app-server',
    version: '1.0.0',
    private: true,
    scripts: {
      start: 'node index.js',
    },
    dependencies: {
      'koa': '^2.15.0',
      '@koa/router': '^13.1.0',
      'koa-body': '^6.0.1',
      'mongoose': '^8.7.0',
    },
  }
}
