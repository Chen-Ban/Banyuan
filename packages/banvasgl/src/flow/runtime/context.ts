/**
 * FlowContext —— 流程执行的运行时环境
 *
 * ═══════════════════════════════════════════════════════════════════
 * 设计定位：FlowContext 是 Flow 解释器的「运行时环境」。
 * ═══════════════════════════════════════════════════════════════════
 *
 * 类比解释器理论：
 *   - 环境（Environment）= 变量绑定表 → getVariable / setVariable
 *   - 职责能力注入 = 系统调用 / 标准库 → env 字段
 *   - 事件参数 = 入口函数的 argv → eventArgs
 *
 * FlowContext 是实现「一套 schema，前后端分别执行」的关键抽象。
 * env 不是在「磨平平台差异」——所有平台（PC/Android/iOS）上都是同一套
 * Web Canvas 代码在跑，平台级差异由壳层 Bridge 注入处理（见 ADR-038）。
 *
 * env 的真正角色是**前后端职责边界的划分**：
 *
 *   前端 env: { navigateTo, playAnimation, markDirty, appId }
 *     → 画布视图操控、动画、页面导航（前端职责）
 *
 *   后端 env: { db, httpClient, appId }
 *     → 数据库访问、外部 HTTP 请求（后端职责）
 *
 * 这是两类完全不同的职责，不是同一件事的不同平台适配。
 * schema 定义意图（做什么），env 界定职责边界（哪端能做什么），
 * executor 将两者连接起来。
 *
 * ═══════════════════════════════════════════════════════════════════
 * Scope 设计 —— Context 层最核心的问题
 * ═══════════════════════════════════════════════════════════════════
 *
 * Context 的设计质量准则：必须完整包含流程执行所需的一切内容。
 * 但「完整」的边界在哪？关键在于 scope（作用域）的粒度设计。
 *
 * scope 决定了：
 *   - 变量在哪些边界内可见（view 级 / page 级 / app 级 / flow 级）
 *   - 不同流程间能否共享状态（隔离性 vs 协作性）
 *   - 上下文切换时哪些数据需要保留/清理
 *
 * 当前实现的 scope 层次：
 *   - 前端: viewId（组件级）→ 'self'（触发者）→ 'page'（页面级）
 *   - 后端: 'local'（单次执行）→ 'flow'（流程级）
 *
 * scope 的粒度直接决定了流程的表达力上限（scope 太小则跨组件通信困难）
 * 和隔离性/安全性的下限（scope 太大则副作用难以追踪）。
 * 这是整个 Flow 架构中最需要根据业务领域审慎权衡的部分。
 *
 * ═══════════════════════════════════════════════════════════════════
 * 设计决策：页面级 Scope + 无事件流机制
 * ═══════════════════════════════════════════════════════════════════
 *
 * 【为什么不需要 DOM 式捕获/冒泡事件流？】
 *
 * Banyan 的视图树与浏览器文档模型表面相似（嵌套容器 + 交互元素），
 * 但事件模型有本质区别。DOM 的捕获/冒泡解决的核心问题是：
 * 深层嵌套节点的事件需要在树的不同层级被拦截和委托。
 * 这是给精通事件委托的前端开发者设计的机制。
 *
 * Banyan 不需要这种机制，原因有三：
 *
 * 1. 用户心智模型不同
 *    Banyan 目标用户是非专业开发者，他们的心智是「点按钮 → 跳页面」
 *    「点图标 → 变颜色」。多层级事件拦截/传播对他们是困惑而非便利。
 *
 * 2. 引擎层已完成事件定位
 *    InteractionStateMachine + resolveActivationTarget 在引擎层做了
 *    「谁接收事件」的决策——命中哪个 View 就触发那个 View 的 events，
 *    这是扁平的「谁被点就触发谁」模型，与 Flow 的语义完全吻合。
 *
 * 3. 可视化编排复杂度
 *    如果引入冒泡，一个 click 可能触发多个 FlowSchema（子 + 父），
 *    执行顺序、stopPropagation、事件对象传递都需要用户在画布上理解
 *    和配置——这对低代码平台的易用性是灾难。
 *
 * 【为什么默认 scope 是页面级（Page/Scene）？】
 *
 * 从 Banyan 设计初衷推导：一个页面就是用户的一个工作单元
 * （一个表单、一个列表页、一个详情页）。
 *
 *   - 页面内组件天然需要互相通信（按钮点击 → 更新另一个组件的文本）
 *   - 跨页面通信通过导航参数传递（navigate 节点的 params）
 *   - 这天然形成了隔离：页面是独立场景，切换时清理局部状态
 *
 * 当前 scope 模型（viewId 级读写 + page 级共享变量）精确对应了
 * 「一个页面是一个独立应用场景」的心智模型。
 *
 * 如果未来需要跨页面共享状态（全局主题、登录态），
 * 应通过 app 级变量或 callFlow 调后端 API 间接实现，
 * 而非扩大默认 scope——避免破坏页面间的隔离性。
 *
 * ═══════════════════════════════════════════════════════════════════
 * 实现约定
 * ═══════════════════════════════════════════════════════════════════
 *
 * 前后端各自提供实现：
 * - 前端 BanvasFlowContext：操作 View.data / Scene.data / navigate / animate
 * - 后端 ServerFlowContext：操作内存变量表 / DB / HTTP
 *
 * FlowRunner 只依赖此接口，不关心具体环境（依赖倒置）。
 */

export interface FlowContext {
  /**
   * 获取变量值
   * @param scope - 变量作用域（前端: viewId/'self'/'page'，后端: 'local'/'flow'）
   * @param key - 变量名
   */
  getVariable(scope: string, key: string): unknown

  /**
   * 设置变量值
   * @param scope - 变量作用域
   * @param key - 变量名
   * @param value - 新值
   */
  setVariable(scope: string, key: string, value: unknown): void

  /** 触发事件时的原始参数列表 */
  eventArgs: unknown[]

  /**
   * 环境特定能力注入
   *
   * 前端可能注入：{ appId, navigateTo, playAnimation, markDirty }
   * 后端可能注入：{ db, appId, httpClient }
   *
   * 执行器通过 ctx.env.xxx 访问环境能力。
   */
  env: Record<string, unknown>
}
